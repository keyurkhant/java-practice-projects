public class GridPoint {

    /**
     * GridPoint class will handle each grid points with x,y co-ordinates of crossword puzzle
     * */
    private int x;
    private int y;

    public GridPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
